// Print ascii value of teh character

// ASCI = American Standard code for Information Exchange

// Every key in our keyboard has an ASCII value for it.

// A -->65
// a -->97
// space --> 32
// 0 --> 48



#include<stdio.h>

int main(){
    char ch;
    printf("Enter any character:  ");
    scanf("%c" , &ch);

    printf("%d",ch);
    
    return 0;
}

